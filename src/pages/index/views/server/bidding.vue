<template>
    <div id="bidding">
        <P class="center first_title">采购方招标操作指南</P>
        <p class="text-indent line-height">采购方通过选择【用户登录】输入账号及密码登陆平台，电机右上角的【我的工作台】进入后台操作界面。选择【招标管理】下的对应功能进行操作。</p>
        <p class="first_title bold" >1、项目管理</p>
        <p class="text-indent second_title">1) 新增项目</p>
        <p class="text-indent line-height">在【项目管理】页面可发起新的项目招标，在状态列表中，可查看项目进展的状态。</p>
        <img class="img_width" src="../../assets/images/bidding/img10.png" alt="">
        <p class="text-indent line-height">点击【新增项目】，填写项目的基本信息，可添加多个配送区域，选择采购类别，然后添加招标的商品，根据不同片区输入不同的预估采购数量。</p>
        <p class="text-indent line-height">可点击保存再编辑提交或也可直接进行提交。</p>
        <img class="img_width" src="../../assets/images/bidding/img1.png" alt="">
        <img class="img_width" src="../../assets/images/bidding/img2.png" alt="">
        <p class="text-indent second_title">2) 审核</p>
        <p class="text-indent line-height">项目提交完成后，点击审核项目即审核通过。若需驳回则点击驳回按钮重新填写项目信息。</p>
        <p class="first_title bold">2、采购文件管理</p>
        <p class="text-indent line-height">项目审核完成后，即可制作采购文件，在项目列表中可点击制作或者点击采购文件管理，找到对应的项目点击【制作】按钮，即可对采购文件进行制作。</p>
        <img class="img_width" src="../../assets/images/bidding/img4.png" alt="">
        <p class="second_title bold">1) 制作</p>
        <p class="text-indent second_title bold">① 公告信息</p>
        <p class="text-indent line-height">首先填写公告信息的内容，公告信息中必填信息填写完成后点击保存。</p>
        <img class="img_width" src="../../assets/images/bidding/img5.png" alt="">
        <p class="text-indent second_title bold">② 资格审查要求</p>
        <p class="text-indent line-height">填写审查要求，资格审查要求非必填。</p>
        <p class="text-indent line-height">在资格审查页面可点击添加行，然后输入对应的审查内容具备的条件说明及判定依据，资格审查内容填写完成后点击保存。</p>
        <img class="img_width" src="../../assets/images/bidding/img6.png" alt="">
        <p class="text-indent second_title bold">③ 价格评标方法</p>
        <p class="text-indent line-height">在价格评标方法页面，在最高得分处可输入报价总得分，然后选择计算公式，并选择对应的基准价及评标方法，基准价和评标方法是相互关联的。价格评标方法填写完成后，可点击保存。</p>
        <img class="img_width" src="../../assets/images/bidding/img7.png" alt="">
        <p class="text-indent second_title bold">④ 资质评分要求</p>
        <p class="text-indent line-height">在资质评分要求页面可点击添加行，输入评分项目、评分标准、判定依据及最高得分。点击保存。资质评分的最高得分和价格最高得分合计等于100。</p>
        <img class="img_width" src="../../assets/images/bidding/img8.png" alt="">
        <p class="text-indent second_title bold">⑤项目附件</p>
        <p class="text-indent line-height">在项目附件中可点击【生成采购文件】，即可在线生成采购文件并下载采购文件，在本地打开查看，点击【上传文件】将采购文件上传。</p>
        <img class="img_width" src="../../assets/images/bidding/img9.png" alt="">
        <p class="second_title bold">2)提交</p>
        <p class="text-indent line-height">将所有的采购文件的内容编辑、保存好后，可点击提交将采购文件提交。</p>
        <p class="second_title bold">3)审核</p>
        <p class="text-indent line-height">在【采购文件管理】列表中点击审核，即可审核此项目的采购文件。</p>
        <p class="text-indent line-height">审核完成后，同步发布项目的采购公告，同时供应商在【获取采购文件管理】页面可对项目的采购文件进行获取。</p>
        <p class="first_title bold">3、获取采购文件管理</p>
        <p class="line-height">在【获取采购文件管理】界面可对供应商的获取信息进行查询、审核。</p>
        <p class="line-height">可选择对应的项目，点击【查看获取信息】。</p>
        <img class="img_width" src="../../assets/images/bidding/img11.png" alt="">
        <p class="text-indent line-height">在获取信息界面，可选择不同的供应商审核其上传的获取信息，保存是否通过审核。将供应商审核通过后点击发送采购文件。</p>
        <img class="img_width" src="../../assets/images/bidding/img3.png" alt="">
        <img class="img_width" src="../../assets/images/bidding/img12.png" alt="">
        <p class="text-indent line-height">若此时获取采购文件供应商数量不满足要求，可点击【流标】，输入流标原因，上传附件信息，点击确定，即项目已经流标，同时发布流标公告。</p>
        <img class="img_width" src="../../assets/images/bidding/img21.png" alt="">
        <p class="first_title bold">4、专家抽取</p>
        <p class="text-indent line-height">在专家抽取中，可点击对应的项目，点击【抽取】，打开抽取列表，选择抽取类型、评标人数及评标所需时间后点击选择专家，即从专家库中选择本项目参与的专家，点击保存。</p>
        <img class="img_width" src="../../assets/images/bidding/img15.png" alt="">
        <img class="img_width" src="../../assets/images/bidding/img16.png" alt="">
        <p class="first_title bold">5、开标评标管理</p>
        <p class="text-indent line-height">在【开标评标管理】页面可进行开标管理，选择对应的项目，点击【开标评标】按钮，进入开标评标界面。（注：在开标时间未到时，此按钮不显示）</p>
        <img class="img_width" src="../../assets/images/bidding/img17.png" alt="">

        <p class="second_title ">1、评前准备</p>
        <p class="text-indent second_title bold">① 投标文件签收解密</p>
        <p class="line-height">点击【投标文件的签收、解密】，进入【投标文件解密】页面。</p>
        <img class="img_center" src="../../assets/images/bidding/img18.png" alt="">

        <p class="text-indent line-height">在投标文件解密页面，可查看提交投标文件的供应商家数，点击【开始解密】，显示解密结束时间。</p>
        <p class="text-indent line-height">在供应商开标管理界面，在30分钟之内完成解密。</p>
        <p class="text-indent line-height">供应商完成解密后，相应的状态为【已解密】，同时显示解密时间。</p>
        <p class="text-indent line-height">在所有供应商都解密完成或者解密时间结束，可【开启标书】。</p>
        <img class="img_width" src="../../assets/images/bidding/img19.png" alt="">
        <p class="text-indent line-height">若此时供应商解密数量或者参与的供应商数量不满足要求，可点击【流标】，输入流标原因，上传附件信息，点击确定，即项目已经流标，同时发布流标公告。</p>
        <img class="img_width" src="../../assets/images/bidding/img21.png" alt="">
        <p class="text-indent second_title bold">② 评审小组设置</p>
        <p>在评审小组设置界面可设置评审参与的专家信息，专家是否参加及专家组长。设置完成后点击保存。专家可登陆系统对参与评标的项目进行【签到】及【评审】。</p>
        <img class="img_width" src="../../assets/images/bidding/img20.png" alt="">

        <p class="second_title">2、开标评标</p>
        <p class="text-indent second_title bold">① 开标记录</p>
        <p class="text-indent line-height">采购方点击开标记录进入开标评标评审界面，点击【下一步】，专家才可点击【评审】进入评审页面</p>
        <img class="img_width" src="../../assets/images/bidding/img23.png" alt="">
        <p class="text-indent second_title bold">② 资格审查</p>
        <p class="">在资格审查页面，采购方可查看各专家提交上的评审结果信息。点击【刷新】，页面信息更新。</p>
        <img class="img_width" src="../../assets/images/bidding/img24.png" alt="">
        <p class="text-indent second_title bold">③ 符合性审查</p>
        <p class="text-indent line-height">专家进入符合性评审后，采购方才可点击【符合性评审】查看专家的评审结果，点击【刷新】，页面信息更新。</p>
        <img class="img_width" src="../../assets/images/bidding/img25.png" alt="">

        <p class="text-indent second_title bold">④ 商务技术评分</p>
        <p class="text-indent line-height">专家进入商务技术评分环节后，采购方才可点击【商务技术评分】查看专家的评审结果，在【商务技术评分】环节，可查看各专家对各供应商的评审结果。点击【刷新】，页面信息更新。</p>
        <img class="img_width" src="../../assets/images/bidding/img26.png" alt="">
        <p class="text-indent second_title bold">⑤ 商务技术评分汇总</p>
        <p class="text-indent line-height">专家进入商务技术评分汇总环节后，采购方才可点击【商务技术评分汇总】查看专家的评审结果。点击【刷新】，页面信息更新。</p>
        <p class="text-indent line-height">采购方点击【下一步】按钮，进入【商务技术结果公布】环节</p>
        <img class="img_width" src="../../assets/images/bidding/img28.png" alt="">

        <p class="text-indent second_title bold">⑥ 商务技术结果公布</p>
        <p class="text-indent line-height">在【商务技术结果公布】页面，采购方可公布商务结果信息。点击【下一步】进入【开标记录（报价）】界面。</p>
        <img class="img_width" src="../../assets/images/bidding/img29.png" alt="">

        <p class="text-indent second_title bold">⑦ 开标记录（报价）</p>
        <p class="text-indent line-height">进入【开标记录（报价）】界面，点击【开启报价文件】后，可显示供应商的报价信息，点击【下一步】专家可进入【报价评审】界面进行评审。</p>
        <img class="img_width" src="../../assets/images/bidding/img13.png" alt="">

        <p class="text-indent second_title bold">⑧ 报价评审</p>
        <p  class="text-indent line-height">在【报价评审】界面，采购方可查看专家对供应商报价的有效性的评审结果。点击【刷新】，页面信息更新。</p>
        <p class="text-indent second_title bold">⑨ 得分汇总</p>
        <p class="text-indent line-height">专家进入得分汇总环节后，采购方才可点击【得分汇总】查看评审结果。点击【刷新】，页面信息更新。</p>
        <p class="text-indent line-height">可查看并下载【中标执行价】，确定供应商是否同意中标，点击【下一步】进行结果公布页面。</p>
        <p class="text-indent second_title bold">⑩结果公布</p>
        <p class="text-indent line-height">在结果公布页面，采购方确认供应商配送的片区。</p>
        <p class="second_title">3、评审结果</p>
        <p class="text-indent line-height">评审完成后，项目状态为待采购方确认，采购方可在评审结果中上传附件。</p>
        <p class="text-indent line-height">在评审结果中上传评审结果附件及采购人结果确认附件，附件上传完成后，点击【提交】即将评审提交。</p>
        <p class="text-indent line-height">提交完成后项目的中标结果公示同步发出。</p>
        <p class="first_title bold">6、公告管理</p>
        <p class="text-indent line-height">采购方在【公告管理】页面可查看公告信息，可点击【详情】查看。或者从前台页面查看公告信息。</p>
        <img class="img_width" src="../../assets/images/bidding/img30.png" alt="">
        <p class="first_title bold">7、合同管理</p>
        <p class="text-indent line-height">项目提交公告信息后，3个工作日后自动生成待起草的合同。</p>
        <p class="text-indent line-height">采购方点击【起草】，即打开起草合同页面，提交后，供应商在线可对合同确认，待采购方和供应商双方将合同确认完成后，合同状态为已确认。</p>
        <p class="text-indent line-height">合同量达到整体采购数量的90%以上，有消息提醒，可点击【结束合同】按钮，结束合同。</p>
