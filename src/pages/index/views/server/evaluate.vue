<template>
  <div id="evaluate">
    <p class="center first_title">后勤物资采购云平台供应商投标操作指南</p>
    <p class="text-indent line-height">项目开标后，评审专家登录后勤物资采购云平台，对项目进行在线评审。 <a href="http://smart.acuit.net/bid/#/index">（http://smart.acuit.net/bid/#/index）</a></p>
    <p class="first_title bold">一、登录 </p>
    <p class="text-indent line-height">专家点击【用户登录】，选择【验证码登录】方式，输入手机号，获取验证码登录。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_25.png" alt="">
    <p>&nbsp;</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_24.png" alt="">
    <p class="first_title bold">二、签到</p>
    <p class="line-height">1.专家进入招标项目评审页面后，找到需要评审的项目，先进行签到；</p>
    <p class="line-height">2.选择在“待评审”标签页下，选择状态为“待签到”的项目，点击操作栏【签到】，显示专家签到页面，填写专家信息，填写完成后点击确定。；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_1.png" alt="">
    <p>&nbsp;</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_12.png" alt="">
    <p class="first_title bold">三、评审</p>
    <p class="line-height">签到完成后，项目状态为“待评审”，点击操作栏【评审】，进入评审页面；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_3.png" alt="">
    <p class="line-height">1.资格审查</p>
    <p class="line-height">1) 进入“评审”页面，第一步进入【资格审查】环节，专家可在线直接【下载投标文件】和【下载采购文件】，点击【在线资格评审】可对供应商进行在线资格评审；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_4.png" alt="">
    <p class="line-height">2) 在【在线资格评审】页面</p>
    <p class="line-height">①点击“切换供应商”，选择对应供应商的投标文件；</p>
    <p class="line-height">②在左侧查看审查内容，右侧显示对应的附件信息；</p>
    <p class="line-height">③对供应商的资格审查进行评定；</p>
    <p class="line-height">④点击保存即将此供应商资格审查评审保存；</p>
    <p class="line-height">⑤切换供应商可继续对其他供应商进行评审。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_13.png" alt="">
    <p class="line-height">3) 【在线资格评审】页面关闭后，回到评审主页面，填写专家评审意见，完成后，可点击右上角的【保存】，即将评审信息进行在线保存，最后进行评审结果的提交，专家点击右上角【提交】，即将此环节评审结果提交；</p>
    <p class="line-height">4) 作为评审小组组长，待所有专家提交评审结果后，评审小组组长在此环节页面中可点击【下一步】，所有专家点击页面的【刷新】按钮后再点击进入下一环节评审，否则不可进入下一评审环节，提示“尚未进行到此阶段”。</p>
    <p class="line-height">2.符合性评审</p>
    <p class="line-height">1)进入【符合性评审】环节，专家可在线浏览查看供应商的投标文件，对供应商投标文件的符合性进行评审，填写说明及专家评审意见，完成后点击右上角的【提交】，即将评审结果提交；</p>
    <p class="line-height">2)作为评审小组组长，待所有专家提交评审结果后，评审小组组长在此环节页面中可点击【下一步】，所有专家都可进入下一环节评审，否则不可进入下一评审环节，提示“尚未进行到此阶段”。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_6.png" alt="">
    <p class="line-height">3.商务技术评分</p>
    <p class="line-height">1)进入【商务技术评分】环节，点击页面上方【在线电子评标】；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_7.png" alt="">
    <p class="line-height">2) 在【在线电子评标】页面</p>
    <p class="line-height">①点击“切换供应商”，选择对应供应商的投标文件；</p>
    <p class="line-height">②在左侧查看评分项，右侧显示对应的附件信息；</p>
    <p class="line-height">③对供应商评分项内容进行打分；</p>
    <p class="line-height">④点击保存即将此供应商评审分数保存；</p>
    <p class="line-height">⑤切换供应商再对其他供应商进行评审。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_11.png" alt="">
    <p class="line-height">3) 回到在主页面，在“评审意见”栏，填写评审意见后，点击右上角【提交】，即将评审结果提交；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_9.png" alt="">
    <p class="line-height">4) 作为评审小组组长，待所有专家提交评审结果后，评审小组组长在此环节页面中可点击【下一步】，所有专家点击页面的【刷新】按钮后再点击进入下一环节评审，否则不可进入下一评审环节，提示“尚未进行到此阶段。</p>
    <p class="line-height">4.商务技术评分汇总</p>
    <p class="line-height">1) 进入【商务技术评分汇总】环节，可查看各专家打分情况及评审意见；</p>
    <p class="line-height">2) 作为评审小组组长确认分数无误后，点击右上角【提交】，提交完成后，采购方可进入【商务技术结果公布】环节；</p>
    <p class="line-height">3) 在采购方开启价格标书之后，评审小组组长可点击【下一步】进入【报价评审】环节；</p>
    <p class="line-height">4) 如专家在此环节需要对评分分数修改，评审小组组长可点击【退回专家打分】，所有专家可重新回到【商务技术评分环节】打分。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_16.png" alt="">
    <p class="line-height">5.报价评审</p>
    <p class="line-height">1) 进入【报价评审】环节，可对供应商的报价信息进行评审，点击页面上方的【在线报价评审】；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_17.png" alt="">
    <p class="line-height">2) 在【在线报价评审】页面，选择对应的供应商，评审其报价是否有效，点击保存即可保存对供应商的评审信息；</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_2.png" alt="">
    <p class="line-height">3) 回到【报价评审】主页面，点击右上角【提交】，即将评审结果提交；</p>
    <p class="line-height">4) 作为评审小组组长，待所有专家提交评审结果后，评审小组组长在此环节页面中可点击【下一步】，所有专家点击页面的【刷新】按钮后再点击进入下一环节评审，否则不可进入下一评审环节，提示“尚未进行到此阶段。</p>
    <p class="line-height">6. 得分汇总</p>
    <p class="line-height">1) 进入【得分汇总】环节，作为评审小组组长，可勾选推荐的中标供应商，并填写比较和评价意见，点击提交，即完成评标；</p>
    <p class="line-height">2) 此环节若供应商未应标则此供应商不可勾选推荐；</p>
    <p class="line-height">3) 其他专家成员在此环节点击【刷新】可查看专家组长推荐的供应商的信息。</p>
    <img class="img_width img_center" src="../../assets/images/evaluate/img_20.png" alt="">
    <p class="line-height">7.评审结束</p>
    <p class="line-height">所有标项评审完成，项目的状态显示为“评审结束”。</p>
    <p class="first_title bold">四、废标</p>
    <p class="line-height">1.在评标过程中，所有环节可进行废标操作；</p>
    <p class="line-height">2.由评审小组组长发起废标操作，组长点击【废标按钮】，在【废标】界面填写废标原因，以及是否同意废标及废标意见，点击提交；</p>
    <p class="line-height">3.评审小组组员需在同一废标环节选择已经由组长发起废标的供应商后的【废标】按钮，填写上废标意见，点击提交；</p>
    <p class="line-height">4.若在某一环节专家未对供应商的废标进行处理则在当前评审环节的提交时会提示“存在供应商在符合性审查环节被标记为废标，请先处理”；</p>
    <p class="line-height">5.待所有专家的废标意见发表完成后，若超过半数的专家判定供应商是废标，则此供应商的状态为“已废标”，若超过半数的专家判定供应商是不废标，则此供应商的状态为“未废标”；</p>
    <p class="line-height">6.在上一环节废标的供应商不进入下一环节的评审。</p>
